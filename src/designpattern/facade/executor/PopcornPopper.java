package designpattern.facade.executor;

/**
 * 爆米花机
 */
public class PopcornPopper {

    public void on(){
        System.out.println("popcorn popper on");
    }

    public void pop(){
        System.out.println("popcorn pop !!!");
    }

    public void off(){
        System.out.println("popcorn popper off");
    }

}
